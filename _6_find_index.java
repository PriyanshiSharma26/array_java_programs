//find out the index position of an element


public class _6_find_index
 {
    public static void main(String[] args)
     {
       int arry[]={10,20,30,40} ;
       int i;
       for(i=0;i<arry.length;i++)
       {
        if(arry[i]==40)
        {
            System.out.println("index position of  "+ arry[i]+" is: " +i);
            break;
        }
    }

       }
    }
    

